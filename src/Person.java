
public class Person {
    private String name;

    // data type the constructor returns is an object
    // the type of object is a Person type
    public Person(String name) {
        this.name = name;
    }

    // getter (gets a value)
    public String getName() {
        return name;
    }

    // setters => sets a value
    public void setName(String name) {
        this.name = name;
    }

}
